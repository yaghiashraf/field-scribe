import { MetadataRoute } from "next";

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: "*",
        allow: "/",
        disallow: ["/dashboard", "/api/"],
      },
    ],
    sitemap: "https://field-scribe.vercel.app/sitemap.xml",
    host: "https://field-scribe.vercel.app",
  };
}
